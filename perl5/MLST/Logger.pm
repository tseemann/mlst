package MLST::Logger;

use base Exporter;
@EXPORT_OK = qw(msg err wrn);

#----------------------------------------------------------------------
our $quiet = 0;

#----------------------------------------------------------------------
sub quiet {
  my($self, $value) = @_;
  $quiet = $value if defined $value;
  return $quiet;
}
#----------------------------------------------------------------------
sub msg {
  print STDERR "@_\n" unless $quiet;
}
#----------------------------------------------------------------------
sub wrn {
  print STDERR "WARNING: @_\n";
}
#----------------------------------------------------------------------
sub err {
  print STDERR "ERRPR: @_\n";
  exit(1);
}
#----------------------------------------------------------------------

1;

